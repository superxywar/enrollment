    <p style="font-size:2em; margin-top:80px;">IPIL DISTRICT ADVENTIST ELEMENTARY SCHOOL, INC.</p>
</div>
<div class="narrative">
    <p><b>"JESUS loves the little childern"</b> was the recurring thought instilled by the MASTER TEACHER in the heart of the late <b>EPENITO GOMEZ FRANCO</b>. Seeing the number of Seventh-Day Adventists children in Ipil SDA Church (now in Ipil Central Adventist Church) at the Ipil Heights (near DPWH) and thinking of his newborn child and 2 stepchildern he dreamed, wished and prayed for a church school to provide Christian Education to these little ones someday.</p>
    <p>That day came when upon the conversion to the Adventist faith and baptism of his stepdaughter <b>ALICE GONZALES DUQUE</b> who was only 16 years old then, together with <b>PTR. TROFIMO DE LEON</b>, Ipil District Leader then, that <b>EPENITO GOMEZ FRANCO</b> opened to her his dreams for a church school right in Ipil District. New to the Seventh-Day Adventist Faith but fully convinced with the love of JESUS in her soul afired with longing for the imminent return of the SAVIOR, she shared her stepfather's sentiment for a Christian education for her younger siblings and children in her new church. Without further thought, considering she received a parcel of lot as inheritance from her biological father the late <b>HERMINIGILDO VALDEZ DUQUE</b>, she readily offered a portion of her lot for the erection of the church & school located at Sanito, Ipil, Zamboanga Sibugay (Zamboanga Del Sur then).</p>
    <p>In February 1976, she signed up the Donation papers for a total land area of 1,000 sq. Meters: 600 sq.m. will go to the school use and 400 sq m for the church property. With 2 teachers- <b>Mrs. EMELIA DE LEON</b> and <b>MISS NELLIE DELA CRUZ (LANGHA)</b> the Ipil Primary SDA School started to cater to more or less 30 pupils from grades 1-4 coming from all over the Ipil District: Ipil-Tugop, Tungawan, Kabasalan. The school was run by the entire Ipil District where the church elder of each local church was to compose the School Board.</p>
    <p><b>EPENITO GOMEZ FRANCO</b>, the visionary, the brain-child of the Ipil District SDA ELEMENTARY School has seen the fulfillment of his long-time dream... lead by Ptr. Trofimo De Leon, the Adventist members in Ipil District came to work every Sunday to build the first school house. Picture the first school house: a 20x40 foot nipa-thatched roof; mangrove trunks for posts; mangrove poles for walls & bare soil fo the floor. Yet both teachers and pupils enjoyed each other's company attending church and praying together during prayer meetings & sabbath worship services, learning inside the classrooms, working the soil together in the garden, playing in the ground, washing together in the nearby river, and doing other fun things together. Those were years of challenges, tears, laughter, fulfillment and growing faith in God's leading.</p>
    <p>Church membership was growing and Ipil Metro Adventist Church (Sanito Adventist Church before) was put up. Since the donated space was growing smaller against the growing number of both churchgoers and students, <b>EPENITO GOMEZ FRANCO</b> once more initiated the request & with Ptr. Rudy Acuna went to <b>ALICE GONZALES DUQUE</b> to possibly add more lot for the donation. <b>MRS. BERNADINA G. FRANCO</b>,  her mother  proposed for a bigger  space for the church too. Seeing thge great need to shelter more lambs & believers, in 1985 she willingly signed up for additional lot donation summing up the previous & recent donation into the total land area  of 2,700 sq m.: with the 1,700 exclusively for the school use and the 1,000 sq m for the church property. It was Alice Duque is to be a perpetual member of the school board and in her absence either Epenito G. Franco or her mother  Bernardina G. Franco to represent.</p>
    <p>IPIL SDA ELEMENTARY SCHOOL was primarily run by the Ipil District. It was during the leadership of PR. RUDY ACUNA & his very active wife teacher MRS. ROSE G. ACUNA Tapping their friends here and abroad and using their own personal resources that the old nipa-hatched roof-mangroove, timber-wall-school- house begun to take a new shape: a better concreted wall & floor & GI Sheet roof. PRAISE GOD FROM WHOM ALL BLESSINGS FLOW! MRS. ROSE G. ACUNA and her teaching force with renewed love and dedication for the MASTER TEACHER taught by role-modelling the littlle lambs who sat under their classes and fed each one with heavenly food from the holy Scriptures and nature then showed them a happy contented life leading to heaven in the LORD JESUS.</p>
    <p>From the simple beginning in 1976 and theenceforth hundreds  of lambs have been graduated – went to high school  and proceeded to college for their degrees. Many of the alumni have contributed to the developmenty of this little Adventist School. The biggest contributor is: MRS. LUZVIMINDA DESIERTO SWANSON now resisding in the U.S.</p>
    <p>More physical development and school progress were seen in the ensuing years under the leadership of MR. GERRY C. FAUSTINO as school board chairman and MRS. ERMA TANGSO-FRANCO as school principal.</p>
    <p>Many of the students who have gone gone of this church school show semblance to the GREAT MASTER TEACHER ready to tell the world of their new-found faith in the soon returning LORD & SAVIOR LORD JESUS CHRIST. Today, those students then, stand as public  and private school teachers, farmers, nurses, medical technologists, engineers, businessmen/women, and others. May they bear in mind, heart and soul their humble beginning at ISDAES where they have learned to love and obey the LORD JESUS CHRIST.</p>
    <p>Behind the progress of the ISDAES and hanging in the memory hall of this little Adventist School house  <b>ARE THE MANY DEDICATED AND COMMITTED ADVENTIST TEACHERS</b> who with willing diligence patiently nurtured and mentored the phisical, mental, spiritual and social aspects of the little ones who have grown up in their hands and left the portals of their dear alma mater to find their places in this world and shine for JESUS.  </p>
